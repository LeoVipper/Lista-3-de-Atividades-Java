package Operadores;

public class Atribuicao {

	public static void main (String args[]) {
		
		int a = 1;
		System.out.println(a);
		
		a += 2;
		System.out.println(a);
		
		a -= 1;
		System.out.println(a);
		
		a *= 3;
		System.out.println(a);
		
		a /= 2;
		System.out.println(a);
		
		a %= 2;
		System.out.println(a);
		
		a += 1;
		System.out.println(a);
		
		a -= 1;
		System.out.println(a);
		
	}
	
}
